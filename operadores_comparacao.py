saldo = 200
saque = 200

print(saldo == saque) # comparação de igualdade, retorna True 
print(saldo != saque) # comparação de desigualdade, retorna False
print(saldo > saque) # comparação de maior que, retorna False
print(saldo < saque) # comparação de menor que, retorna False
print(saldo >= saque) # comparação de maior ou igual, retorna True
print(saldo <= saque) # comparação de menor ou igual, retorna True